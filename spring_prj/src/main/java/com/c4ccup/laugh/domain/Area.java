package com.c4ccup.laugh.domain;

public class Area {

    /** id */
    private int id;
    /** 地方名 */
    private String areaName;

    /**
     * @return id
     */
    public int getId() {
        return id;
    }
    /**
     * @param id セットする id
     */
    public void setId(int id) {
        this.id = id;
    }
    /**
     * @return areaName
     */
    public String getAreaName() {
        return areaName;
    }
    /**
     * @param areaName セットする areaName
     */
    public void setAreaName(String areaName) {
        this.areaName = areaName;
    }
}
